const chosenNumber = 5;

// FOR
for (let multiplier = 1; multiplier <= 10; multiplier++) {
  console.log(`${chosenNumber} x ${multiplier} = ${chosenNumber * multiplier}`);
}

console.log('___________');
console.log();

// WHILE
let multiplier = 1;

while (multiplier <= 10) {
  console.log(`${chosenNumber} x ${multiplier} = ${chosenNumber * multiplier}`);
  multiplier++;
}
